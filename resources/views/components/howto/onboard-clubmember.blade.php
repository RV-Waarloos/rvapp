<div class="mx-6 border-2 rounded-md p-4 ">
    <div class="text-base font-medium mb-2">Nieuw clublid inschrijven</div>
    <p class="">
        Om een nieuw lid in te schrijven geef je volgende gegevens in: voornaam, chternaam en email adres. Het email adres moet
        uniek zijn en mag nog niet gebruikt worden door een bestaand club lid.
    </p>
    <p class="">
        Wanneer gemachtigd bent om leden in te schrijven voor alle afdelingen, moet aangeven bij welke afdeling dit nieuw lid zal ingeschreven worden.
        Ben je enkel gemachtigd om leden in te schrijven voor je eigen afdeling, dan is deze keuze reeds gemaakt.
    </p>
    <div class="text-base font-medium my-2">Hoe verloopt de inschrijving?</div>
    <p class="">
        Wanneer je de gegevens voor de inschrijving bevestigt, wordt het inschrijvingsproces gestart.
    </p>
    <p class="">
        Het nieuwe lid zal via email een uitnodiging ontvangen om zijn/haar account te activeren (via een beveiligde url in de mail). Tijdens deze activering 
        zal het nieuwe lid een paswoord kiezen en het profiel (adres, telefoonnummer, geboortedatun ...) vervolledigen. Het invullen van deze profielgegevens
        is echter geen verplichting. Eveneens kan er geopteerd worden om de profielgegevens enkel zichtbaar te maken voor beheerders van de RV Waarloos site.
    </p>
    <p class="">
        Bij het activeren van de account moet het nieuwe lid eveneens het reglement van de web site aanvaarden en goedkeuren.
    </p>
    <div class="text-base font-medium my-2">Opvolging</div>
    <p class="">
        Wanneer je een inschrijving start kan je deze opvolgen via de status pagina van de inschrijvingen.
        Je krijgt eveneens een notificatie telkens de toestand van de inschrijving gewijzigd wordt. (gestart, geactiveerd, betaald, afgebroken...)
    </p>
</div>